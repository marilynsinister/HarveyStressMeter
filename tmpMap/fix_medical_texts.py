def apply_replacements(path: Path, pairs: list, label: str) -> int:
    text = path.read_text(encoding="utf-8")
    count = 0
    for old, new in pairs:
        if old not in text:
            continue
        text = text.replace(old, new, 1)
        count += 1
    path.write_text(text, encoding="utf-8")
    print(f"{label}: {count}/{len(pairs)} replacements")
    return count

