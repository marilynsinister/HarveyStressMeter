SPEAK_RE = re.compile(r'(speak Harvey \\"((?:[^"\\]|\\.)*?)\\")', re.DOTALL)
KEY_RE = re.compile(
    r'"(HarveyOverhaul(?:Story|Romance)\.[^"]*Friendship Harvey (\d+)[^"]*)": "',
)


def convert_speak(chunk: str) -> str:
    out = chunk
    for old, new in REPLACEMENTS:
        out = out.replace(old, new)
    return out


def find_string_end(text: str, start: int) -> int:
    i = start
    while i < len(text):
        if text[i] == "\\":
            i += 2
            continue
        if text[i] == '"':
            return i
        i += 1
    raise ValueError("unterminated string")


def main():
    text = EVENTS.read_text(encoding="utf-8")
    edits: list[tuple[int, int, str, str]] = []

    for m in KEY_RE.finditer(text):
        fp = int(m.group(2))
        if fp < 750:
            continue
        eid = m.group(1).split("/")[0]
        body_start = m.end()
        body_end = find_string_end(text, body_start)
        body = text[body_start:body_end]
        new_body = SPEAK_RE.sub(lambda x: convert_speak(x.group(1)), body)
        if new_body != body:
            edits.append((body_start, body_end, new_body, f"{eid} ({fp} FP)"))

    for body_start, body_end, new_body, label in reversed(edits):
        text = text[:body_start] + new_body + text[body_end:]

    EVENTS.write_text(text, encoding="utf-8")
    print(f"Updated {len(edits)} events:")
    for _, _, _, label in edits:
        print(f"  - {label}")


if __name__ == "__main__":
    main()
