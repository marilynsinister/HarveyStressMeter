def apply_replacements(text: str, pairs: list[tuple[str, str]]) -> tuple[str, list[str]]:
    changes: list[str] = []
    out = text
    for old, new in pairs:
        if old in out and old != new:
            out = out.replace(old, new)
            changes.append(f"{old} → {new}")
    return out, changes


def process_event_file(path: Path) -> list[tuple[str, list[str]]]:
    if not path.exists():
        return []
    text = path.read_text(encoding="utf-8")
    log: list[tuple[str, list[str]]] = []

    def repl(m: re.Match) -> str:
        key, body = m.group(1), m.group(2)
        event_id = key.split("/")[0]
        romantic = bool(EVENT_ROMANCE.search(key))
        pairs = ROMANCE_ONLY if romantic else NEUTRAL
        new_body, changes = apply_replacements(body, pairs)
        if changes:
            log.append((f"{path.name}::{event_id}", changes))
        return f'"{key}": "{new_body}"'

    new_text = re.sub(
        r'"((?:[^"\\]|\\.)+)":\s*"((?:[^"\\]|\\.)*)"',
        repl,
        text,
        flags=re.S,
    )
    if new_text != text:
        path.write_text(new_text, encoding="utf-8")
    return log


def process_dialogue_file(path: Path) -> list[tuple[str, list[str]]]:
    if not path.exists():
        return []
    text = path.read_text(encoding="utf-8")
    log: list[tuple[str, list[str]]] = []

    parts = re.split(r'(\{\s*\n\s*"Action":\s*"EditData")', text)
    out_parts = [parts[0]]
    idx = 1
    while idx < len(parts):
        head = parts[idx]
        body = parts[idx + 1] if idx + 1 < len(parts) else ""
        block = head + body
        idx += 2
        romantic = WHEN_ROMANCE.search(block) is not None
        pairs = ROMANCE_ONLY if romantic else NEUTRAL

        def repl_val(m: re.Match) -> str:
            key, val = m.group(1), m.group(2)
            new_val, changes = apply_replacements(val, pairs)
            if changes:
                log.append((f"{path.name}::{key}", changes))
            return f'"{key}": "{new_val}"'

        if '"Target"' in block and "Harvey" not in block:
            new_block = block
        else:
            new_block = re.sub(
                r'"([^"]+)":\s*"((?:[^"\\]|\\.)*)"',
                repl_val,
                block,
                flags=re.S,
            )
        out_parts.append(new_block)

    new_text = "".join(out_parts)
    if new_text != text:
        path.write_text(new_text, encoding="utf-8")
    return log


def main() -> None:
    all_log: list[tuple[str, list[str]]] = []
    for p in EVENT_FILES:
        all_log.extend(process_event_file(p))
    for p in DIALOGUE_FILES:
        all_log.extend(process_dialogue_file(p))

    scenes = sorted({scene for scene, _ in all_log})
    lines = [
        "# Контролирующие реплики — замены",
        "",
        f"Всего затронуто записей: **{len(all_log)}**",
        f"Уникальных сцен/ключей: **{len(scenes)}**",
        "",
        "## Изменённые сцены и ключи",
        "",
    ]
    for scene in scenes:
        changes = next(c for s, c in all_log if s == scene)
        lines.append(f"### `{scene}`")
        for ch in changes[:8]:
            lines.append(f"- {ch}")
        if len(changes) > 8:
            lines.append(f"- … ещё {len(changes) - 8} замен")
        lines.append("")

    REPORT.parent.mkdir(parents=True, exist_ok=True)
    REPORT.write_text("\n".join(lines), encoding="utf-8")
    print(f"Updated {len(all_log)} entries across {len(scenes)} scenes/keys")
    print(f"Report: {REPORT}")


if __name__ == "__main__":
    main()
