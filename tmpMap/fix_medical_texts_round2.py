def apply(path, pairs):
    text = path.read_text(encoding="utf-8")
    n = 0
    for item in pairs:
        if len(item) == 3:
            old, new, _ = item
            if old in text:
                text = text.replace(old, new)
                n += 1
        else:
            old, new = item
            if old in text:
                text = text.replace(old, new, 1)
                n += 1
    path.write_text(text, encoding="utf-8")
    print(f"{path.name}: {n}/{len(pairs)}")

